/** ISO-8601 строка даты/времени — то, как дата летит по HTTP. */
export type ISODateString = string

/**
 * Уровень астро-расчёта:
 * - `sun` — только по знаку (нужна лишь дата рождения), Уровень A;
 * - `chart` — по натальной карте (дата + время рождения, опц. место), Уровень B.
 */
export type AstroLevel = 'sun' | 'chart'

/**
 * Данные рождения. Время и место — опциональны: без времени считаем по знаку
 * (Уровень A), со временем поднимаемся до карты (Луна/Меркурий/Венера/Марс, Уровень B).
 * Место (lat/lon) уточняет часовой пояс (а в будущем — асцендент).
 */
export interface BirthInput {
  birthday: ISODateString
  /** Локальное время рождения 'HH:MM' → включает Уровень B. */
  birthTime?: string
  /** Широта места рождения (для асцендента). */
  lat?: number
  /** Долгота места рождения (для асцендента / звёздного времени). */
  lon?: number
  /** Часовой пояс места рождения (смещение от UTC в часах). */
  tz?: number
}
